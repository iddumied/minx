#ifndef __MINX_DIM_H__
#define __MINX_DIM_H__

/*
 *
 * Defining *byte sizes here
 *
 */

#define BYTE        1
#define KILOBYTE    (1000 * BYTE)
#define MEGABYTE    (1000 * KILOBYTE)
#define GIGABYTE    (1000 * MEGABYTE)

#define KIBIBYTE    (1024 * BYTE)
#define MEBIBYTE    (1024 * KIBIBYTE)
#define GIBIBYTE    (1024 * MEBIBYTE)

#endif // __MINX_DIM_H__
