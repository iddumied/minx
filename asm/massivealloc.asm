# alloc sizes in registers 

MOVI 0xB0 0x0F
MOVI 0xB1 0x0A
MOVI 0xB2 0x2F
MOVI 0xB3 0x4F
MOVI 0xB4 0xDF

#
# for each alloc size, allocate and move memID to register
# 

ALLOC 0xB0
MOV 0xB0 0x02 

ALLOC 0xB1
MOV 0xB1 0x02 

ALLOC 0xB2
MOV 0xB2 0x02 

ALLOC 0xB3
MOV 0xB3 0x02 

ALLOC 0xB4
MOV 0xB4 0x02 

#
# free all allocated mem
#

FREE 0xB0
FREE 0xB1
FREE 0xB2
FREE 0xB3
FREE 0xB4

#
# exit(0)
#

MOVI 0xB5 0x00
EXIT 0xB5
